import mongoose from "mongoose"
const Dbscheema=new mongoose.Schema({
  firstname:{
    type:String,
    required:true
  },
  lastname:{
    type:String,
    required:true
  },
  email:{
    type:String,
    required:true,
    unique:true
  },
  username:{
    type:String,
    required:true,
    unique:true
  },
  password:{
    type:String,
    required:true
  },
  profileimage:{
    type:String,
    default:""
  },
  coverimage:{
    type:String,
    default:""
  },
  headline:{
    type:String,
    default:""
  },
  skill:[{type:String}],
  education:[
    {
      college:{type:String},
      degree:{type:String},
      fieldofstudy:{type:String}
    }
  ],
  location:{
    type:String,
    default:"India",
  },
  gender:{
    type:String,
    enum:["male","female","other"]
  },
  connection:[
    {type:mongoose.Schema.Types.ObjectId,
      ref:"User"
    }
  ]
},{timestamps:true})
const User=mongoose.model("User",Dbscheema)
export default User
